    void totalHarga (){
        double totalHArga = 0;
        int jumlahBaris = tebel.getRowCount();
        for (int i = 0; i < jumlahBaris; i++){
            totalHArga = totalHArga 
                    + Double.parseDouble(tabelPenjualan.getValueAt(i, 7).toString());
        }
        fieldTotalHarga.setText(String.valueOf(totalHArga));
    }
    
