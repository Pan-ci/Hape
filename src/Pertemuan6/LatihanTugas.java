    private void fieldJumlahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fieldJumlahActionPerformed
        // TODO add your handling code here:
        int hargaJual = Integer.parseInt(fieldHarga.getText());
        int jumlahBeli = Integer.parseInt(fieldJumlah.getText());
        int total = hargaJual * jumlahBeli;
        fieldTotal.setText(String.valueOf(total));
        buttonSimpan.setEnabled(true);
        totalHarga();
    }//GEN-LAST:event_fieldJumlahActionPerformed

    private void tabelPenjualanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPenjualanMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount()==1){
            
        fieldFaktur.setText("");
        fieldTanggal.setText("");
        fieldNamaPelanggan.setText("");
        fieldKode.setText("");
        fieldNamaBarang.setText("");
        fieldHarga.setText("");
        fieldJumlah.setText("");
        fieldTotal.setText("");
        
        baris = tabelPenjualan.getSelectedRow();
        
        fieldFaktur.setText(tebel.getValueAt(baris, 0).toString());
        fieldTanggal.setText(tebel.getValueAt(baris, 1).toString());
        fieldNamaPelanggan.setText(tebel.getValueAt(baris, 2).toString());
        fieldKode.setText(tebel.getValueAt(baris, 3).toString());
        fieldNamaBarang.setText(tebel.getValueAt(baris, 4).toString());
        fieldHarga.setText(tebel.getValueAt(baris, 5).toString());
        fieldJumlah.setText(tebel.getValueAt(baris, 6).toString());
        fieldTotal.setText(tebel.getValueAt(baris, 7).toString());
        
        buttonSimpan.setEnabled(false);
        }
    }//GEN-LAST:event_tabelPenjualanMouseClicked

